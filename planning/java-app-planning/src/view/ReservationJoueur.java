    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String jourString = String.valueOf(jourCombo.getSelectedItem());
        int jour, heure;
        heure = Integer.parseInt(String.valueOf(heureCombo.getSelectedItem()));

        switch (jourString) {
            case "Samedi 15":
                jour = 1;
                break;
            case "Dimanche 16":
                jour = 2;
                break;
            case "Lundi 17":
                jour = 3;
                break;
            case "Mardi 18":
                jour = 4;
                break;
            case "Mercredi 19":
                jour = 5;
                break;
            case "Jeudi 20":
                jour = 6;
                break;
            case "Vendredi 21":
                jour = 7;
                break;
            case "Samedi 22":
                jour = 8;
                break;
            default:
                jour = -1;
        }

        CourtController.getCourtsLibres(jour, heure, courtsModel);
    }//GEN-LAST:event_jButton1ActionPerformed

