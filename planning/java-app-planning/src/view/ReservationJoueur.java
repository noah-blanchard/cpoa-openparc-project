    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int r = jTable1.getSelectedRow();
        int c = Integer.parseInt(String.valueOf(jTable1.getValueAt(r, 0)));
        int h = Integer.parseInt(String.valueOf(jTable1.getValueAt(r, 1)));
        int jour = -1;
        String jourString = String.valueOf(jourCombo.getSelectedItem());;

        switch (jourString) {
            case "Samedi 15":
                jour = 1;
                break;
            case "Dimanche 16":
                jour = 2;
                break;
            case "Lundi 17":
                jour = 3;
                break;
            case "Mardi 18":
                jour = 4;
                break;
            case "Mercredi 19":
                jour = 5;
                break;
            case "Jeudi 20":
                jour = 6;
                break;
            case "Vendredi 21":
                jour = 7;
                break;
            case "Samedi 22":
                jour = 8;
                break;
            default:
                jour = -1;
        }

        JOptionPane pane = new JOptionPane();
        if (pane.showConfirmDialog(this, "Etes-vous sûr ?", "Confirmation", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            if (!CourtController.newReservation(c, jour, h)) {
                JOptionPane.showMessageDialog(this, "Erreur dans la réservation", "Erreur", JOptionPane.ERROR_MESSAGE);
            } else {
                this.dispose();
                JoueurMenu.showFrame();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

